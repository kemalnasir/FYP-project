    private void orderTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_orderTableMouseClicked
        int index = orderTable.getSelectedRow();
        int a = JOptionPane.showConfirmDialog(null, "Do you want to remove this item?", "Select", JOptionPane.YES_NO_OPTION);
        if (a == 0) {
            TableModel model = orderTable.getModel();
            String total = model.getValueAt(index, 5).toString();
            String amount = model.getValueAt(index, 4).toString();
            grandTotal = grandTotal - Double.parseDouble(total);
            quantityOrder -= Integer.parseInt(amount);
            txtTotal.setText(String.valueOf(grandTotal).format("%.2f", grandTotal));
            txtQuantityOrder.setText(String.valueOf(quantityOrder));
            ((DefaultTableModel) orderTable.getModel()).removeRow(index);
        }
    }//GEN-LAST:event_orderTableMouseClicked

