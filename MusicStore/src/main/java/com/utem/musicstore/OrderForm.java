    private void txtQuantityStateChanged(javax.swing.event.ChangeEvent evt) {//GEN-FIRST:event_txtQuantityStateChanged
        int quantity = (Integer) txtQuantity.getValue();
        if (quantity <= 1) {
            txtQuantity.setValue(1);
            quantity = 1;
        }
        itemsTotal = itemsPrice * quantity;
        txtItemsTotal.setText(String.valueOf(itemsTotal).format("%.2f", itemsTotal));
    }//GEN-LAST:event_txtQuantityStateChanged

    private void buttonExactActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonExactActionPerformed
        double cash = Double.parseDouble(txtCash.getText());
        change = cash - grandTotal;
        if(cash < grandTotal)
        {
            double change2 = grandTotal - cash;
            JOptionPane.showMessageDialog(null, "The cash not enough RM " + change2 + "!");
            txtChange.setText("Your Change");
        }
        else
        {
            txtChange.setText("RM " +String.valueOf(change).format("%.2f", change));
        }
    }//GEN-LAST:event_buttonExactActionPerformed

