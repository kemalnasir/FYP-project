    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
         // TODO add your handling code here:
        if (!isEmptyField()) 
        {
            c = new CarBooking ();

            c.setCustomerIc(icTxtField.getText().trim());
            c.setCustomerName(nameTxtField.getText().trim());
            c.setGender((String)genderbox.getSelectedItem());
            c.setUserType((String)userTypebox.getSelectedItem());
            c.setVehicleReg(registerTxtField.getText().trim());
            c.setVehicleName(typeTxtField.getText().trim());
            c.setPrice(Double.parseDouble(priceTxtField.getText().trim()));
            c.setTotalHour(Integer.parseInt(hourTxtField.getText().trim()));
            
            manager.saveItem(c);
            custList.add(c);

            saveBtn.setEnabled(false);
            printBtn.setEnabled(true);
            resetBtn.setEnabled(false);
            exitBtn.setEnabled(true);
            
            JOptionPane.showMessageDialog(null,"Your booking has been saved!");
            clearForm();
        }
        else 
        {
            JOptionPane.showMessageDialog(null,"Please complete the form!");        
        }
    }//GEN-LAST:event_saveBtnActionPerformed

