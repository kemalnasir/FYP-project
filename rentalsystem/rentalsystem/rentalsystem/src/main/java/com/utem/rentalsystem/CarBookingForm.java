    private void searchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBtnActionPerformed
        car = new Car();
        
        //create object to set two -decimal places of price output format
        DecimalFormat df = new DecimalFormat("0.00");

        // Check if the Car No field is empty
        if(registerTxtField.getText().isEmpty()){
            // Display information message box
            JOptionPane.showMessageDialog(null, "Please enter Registration No");
        }
        else{
                car = manage.searchCarBook(String.valueOf(registerTxtField.getText().trim()));
                if(car.getVehicleReg() != null){
                    //Display the car attributes in the form
                    
                    typeTxtField.setText(car.getVehicleName());
                    priceTxtField.setText(String.valueOf(df.format(car.getPrice())));
                    
                    // Set the new state buttons
                    saveBtn.setEnabled(true);
                    searchBtn.setEnabled(true);
                    resetBtn.setEnabled(true);
                    printBtn.setEnabled(true);
                         
                }
                // If do not found the selected car yet
                else{
                initialBtn();
                clearBox();
                JOptionPane.showMessageDialog(null, "Car does not exist!");
            }
        }
    }//GEN-LAST:event_searchBtnActionPerformed

